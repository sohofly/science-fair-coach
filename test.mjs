import {JSDOM} from 'jsdom';
import fs from 'node:fs';
import assert from 'node:assert/strict';
const html=fs.readFileSync('index.html','utf8').replace('<script src="app.js"></script>','');
const dom=new JSDOM(html,{url:'https://example.test/',runScripts:'dangerously'});
dom.window.confirm=()=>true;
dom.window.scrollTo=()=>{};
dom.window.eval(fs.readFileSync('app.js','utf8'));
const {document}=dom.window;
const click=selector=>{const el=document.querySelector(selector);assert.ok(el,`找不到 ${selector}`);el.click()};
click('[data-go="division"]');
assert.match(document.querySelector('#app').textContent,/想參加哪一個科別/);
click('[data-division="biology"]');
click('[data-next="profile"]');
document.querySelector('#grade').value='5';
document.querySelector('#weeks').value='8';
click('[data-interest="植物"]');
click('[data-profile]');
assert.match(document.querySelector('#app').textContent,/推薦的候選題目/);
click('[data-topic="b1"]');
click('[data-next="plan"]');
assert.match(document.querySelector('#app').textContent,/光的方向與豆苗彎曲/);
click('[data-action="teacher"]');
assert.match(document.querySelector('#app').textContent,/教師確認清單/);
assert.equal(JSON.parse(dom.window.localStorage.getItem('scienceFairCoachV1')).topicId,'b1');
console.log('✓ 學生選題、研究摘要、教師檢視與本機儲存皆通過');
